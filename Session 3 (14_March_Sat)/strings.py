# Default value of a string is an empty string ""
# Strings support positive indexing and negative indexing
# Strings are immutable (cannot be changed after creation)

# Creating a string
a = "Miller"

# Using len() to get the length of the string
print(len(a))

# Using type() to check the datatype
print(type(a))


# ============================================
# STRING INDEXING
# ============================================

# Positive indexing starts from 0
# Negative indexing starts from -1 (from the end)

a = "Michael"

# This is used to get a particular alphabet from the string
print(a[5])   # prints 'e'